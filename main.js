// Ejemplos de Suma - Resta - Multiplicacion - Division  

// Ejemplo Suma
let x = 10
let numero = parseInt(prompt("Ingrese un numero"))
let resultadoSuma = x + numero
alert("El resultado de su numero mas 10 es: " + resultadoSuma);

//Ejemplo Resta
let numero2 = parseInt(prompt("Ingrese un numero"))
let resultadoResta = numero2 - x
alert("El resultado de su numero menos 10 es: " + resultadoResta);

//Ejemplo Multiplicacion
let numero3 = parseInt(prompt("Ingrese un numero"))
let resultadoMultiplicacion = numero3 * x
alert("El resultado de su numero por 10 es: " + resultadoMultiplicacion);

//Ejemplo Division
let numero4 = parseInt(prompt("Ingrese su numero"))
let resultadoDivision = numero4 / x
alert("El resultado de su numero dividido 10 es: " + resultadoDivision);

